                    <p>A história começa como todo mundo já sabe: quando Bruce Wayne tinha oito anos de idade e viu os pais serem assassinados na sua frente. Após esse acontecimento, ele acaba sendo criado por seu tutor, Alfred, que também é o mordomo da família. Os anos se passaram e Bruce resolve dedicar sua vida a combater o crime. Aos 14 anos, ele viajou pelo mundo, frequentando 8 universidades e, visitando o Oriente para treinar artes maciais. Nesse período, ele também aprendeu técnicas de cura, caça e infiltração. Já com 26 anos, ele retorna a Gotham, e assume a identidade de Batman.</p>
                    <p>Com a ajuda das Industrias Wayne, Bruce conseguiu desenvolver todos os aparatos para o Morcego. Entre suas invenções mais famosas, destacam-se os veículos. Uma de suas primeiras invenções foi uma asa delta, seguida de uma moto e, por ultimo, o conhecido Batmóvel. Tudo sendo guardado em sua base secreta, embaixo da Mansão Wayne.</p>
                    
